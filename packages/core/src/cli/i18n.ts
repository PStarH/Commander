/**
 * Commander CLI i18n — Lightweight internationalization.
 *
 * WARNING: All translations are hardcoded in this 2,700+ line TypeScript file.
 * Translators cannot edit without TypeScript tooling. The file must be recompiled
 * for any translation change. No lazy loading of locale files.
 *
 * Supports en (English) and zh-CN (Simplified Chinese).
 * Detects locale from LANG, LC_ALL, or COMMANDER_LANG env vars.
 *
 * Usage:
 *   import { t } from './i18n';
 *   console.log(t('help.title'));  // "Commander" or "Commander — 多智能体编排系统"
 *
 * Interpolation: t('key', { name: 'value' }) substitutes {name}.
 *
 * Coverage (post-expansion):
 *   - Original 60 keys preserved verbatim
 *   - ~280 new keys covering hardcoded English in util.ts, envLoader.ts,
 *     cliRateLimiter.ts, repl.ts and every commands/*.ts file
 *   - 19 namespaces total
 *
 * Lookup behaviour: missing locale key falls back to en, which falls back
 * to the key itself. This means untranslated keys NEVER throw — they
 * silently degrade to English.
 */

// ============================================================================
// Locale detection
// ============================================================================

type Locale = 'en' | 'zh-CN';

function detectLocale(): Locale {
  const lang = process.env.COMMANDER_LANG || process.env.LANG || process.env.LC_ALL || '';
  if (lang.startsWith('zh')) return 'zh-CN';
  return 'en';
}

