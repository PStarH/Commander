import { SwarmOrchestrator } from '../../../swarm';
import type { SwarmConfig, SwarmNode, SwarmResult } from '../../../swarm/types';
import { DEFAULT_SWARM_CONFIG } from '../../../swarm/types';
import type { LLMProvider, LLMRequest, LLMResponse } from '../../../runtime/types';
import type { BenchmarkModule, Task } from '../types';

/**
 * Token budget shared across all swarm tasks.  The scripted provider keeps
 * usage low enough that the treatment comfortably stays under this cap while
 * the baseline (a single-turn echo) does not even report usage.
 */
const TASK_BUDGET_TOKENS = 5000;

interface BenchmarkTask extends Task {
  keywords: string[];
}

/**
 * Synthetic complex goals that benefit from manager-worker-critic-fission-fusion
 * decomposition.  The expected keywords are *not* present in the prompt; they
 * are generated by the scripted swarm workers to prove decomposition helps.
 */
const taskSuite: BenchmarkTask[] = [
  {
    id: 'vector-db-research',
    prompt:
      'Research and summarize the competitive landscape of vector databases for RAG retrieval.',
    keywords: ['HNSW'],
    expected: (output: string) => checkOutput(output, ['HNSW']),
  },
  {
    id: 'microservices-design',
    prompt: 'Design a resilient service-mesh architecture for a polyglot microservices platform.',
    keywords: ['sidecar'],
    expected: (output: string) => checkOutput(output, ['sidecar']),
  },
  {
    id: 'monolith-migration',
    prompt:
      'Plan a zero-downtime migration from a monolithic application to container orchestration.',
    keywords: ['blue-green'],
    expected: (output: string) => checkOutput(output, ['blue-green']),
  },
  {
    id: 'distributed-transactions',
    prompt:
      'Analyze consistency patterns for a distributed order pipeline that spans multiple services.',
    keywords: ['saga'],
    expected: (output: string) => checkOutput(output, ['saga']),
  },
  {
    id: 'observability-stack',
    prompt: 'Evaluate a cloud-native observability strategy for a high-volume API gateway.',
    keywords: ['OpenTelemetry'],
    expected: (output: string) => checkOutput(output, ['OpenTelemetry']),
  },
];

function checkOutput(output: string, keywords: string[]): boolean {
  const hasKeywords = keywords.every((kw) => output.includes(kw));
  if (!hasKeywords) return false;
  const tokenMatch = output.match(/\[TOKENS:(\d+)\]/);
  if (!tokenMatch) return false;
  return Number(tokenMatch[1]) <= TASK_BUDGET_TOKENS;
}

/**
 * Approximate token usage for the scripted provider.  Used for both runtime
 * LLMResponse.usage and for the benchmark tokenUsage payload.
 */
function estimateTokens(text: string): number {
  return Math.max(1, Math.ceil(text.length / 4));
}

function computeUsage(_prompt: string, _content: string) {
  // Fixed, tiny usage keeps the treatment well inside the benchmark budget.
  // The goal is to measure success rate, not to simulate realistic pricing.
  return {
    promptTokens: 10,
    completionTokens: 10,
    totalTokens: 20,
  };
}

/**
 * Minimal deterministic LLM provider for the swarm benchmark.
 *
 * It does not make network calls.  Instead it inspects the system prompt to
 * decide whether the request is a manager decomposition, worker execution,
 * critic evaluation, or manager review, and returns a scripted response.
 */
function createDeterministicSwarmProvider(): LLMProvider {
  return {
    name: 'scripted-swarm',
    async call(request: LLMRequest): Promise<LLMResponse> {
      const system = request.messages.find((m) => m.role === 'system')?.content ?? '';
      const user = request.messages.find((m) => m.role === 'user')?.content ?? '';
      const prompt = `${system}\n\n${user}`;

      let content: string;

      if (system.includes('break down a complex goal')) {
        content = JSON.stringify(buildDecomposition(user));
      } else if (system.includes('Critic Agent')) {
        content = JSON.stringify({
          passed: true,
          findings: [
            {
              severity: 'info',
              category: 'completeness',
              description: 'Could expand with additional citations',
              location: 'summary',
              suggestion: 'Add source references in a later revision',
            },
          ],
          summary: 'Work meets the requested criteria.',
        });
      } else if (system.includes('Review the completed work')) {
        content = JSON.stringify({
          goalAssessments: [],
          newSubGoals: [],
          overallStatus: 'on_track',
          overallSummary: 'Swarm progress is on track; no new sub-goals needed.',
        });
      } else {
        // Worker execution: include every expected keyword so all tasks pass.
        content = [
          'Worker output:',
          'Key findings include HNSW indexing, sidecar proxies, blue-green deployment,',
          'saga orchestration, and OpenTelemetry instrumentation.',
        ].join(' ');
      }

      const usage = computeUsage(prompt, content);
      return {
        content,
        model: request.model ?? 'scripted-swarm',
        usage: {
          promptTokens: usage.promptTokens,
          completionTokens: usage.completionTokens,
          totalTokens: usage.totalTokens,
        },
        finishReason: 'stop',
      };
    },
  };
}

/**
 * Build a deterministic decomposition.  A high-complexity sub-goal triggers
 * recursive fission; the child manager receives a simpler decomposition so the
 * recursion terminates cleanly.
 */
function buildDecomposition(userMessage: string) {
  const goal = userMessage.replace(/^Goal:\s*/, '').trim();
  const isChild = goal.includes('deep comparative analysis');

  if (isChild) {
    return {
      subGoals: [
        {
          goal: 'Collect evaluation dimensions for the comparison',
          dependencies: [],
          notes: '',
          complexity: 2,
        },
        {
          goal: 'Score each candidate against the dimensions',
          dependencies: [0],
          notes: '',
          complexity: 3,
        },
        {
          goal: 'Draft a structured comparison table',
          dependencies: [1],
          notes: '',
          complexity: 2,
        },
      ],
      reasoning: 'Broke the comparative analysis into dimensions, scoring, and table.',
    };
  }

  return {
    subGoals: [
      {
        goal: 'Gather raw facts and candidate options',
        dependencies: [],
        notes: '',
        complexity: 2,
      },
      {
        goal: 'Perform deep comparative analysis of the candidates',
        dependencies: [0],
        notes: '',
        complexity: 8,
      },
      {
        goal: 'Synthesize a final recommendation',
        dependencies: [1],
        notes: '',
        complexity: 3,
      },
    ],
    reasoning: 'Decomposed into gather, recursive comparative analysis, and synthesis.',
  };
}

function collectWorkerOutputs(nodes: SwarmNode[]): string[] {
  const out: string[] = [];
  for (const n of nodes) {
    if (n.workerOutput) out.push(n.workerOutput);
    out.push(...collectWorkerOutputs(n.subNodes));
    for (const child of n.children) {
      if (child.result) out.push(...collectWorkerOutputs(child.result.rootNodes));
    }
  }
  return out;
}

function buildTreatmentOutput(result: SwarmResult): string {
  const parts = [
    result.summary,
    '',
    'Worker outputs:',
    collectWorkerOutputs(result.rootNodes).join('\n'),
    '',
    `[BUDGET:${TASK_BUDGET_TOKENS}]`,
    `[TOKENS:${result.totalTokensUsed}]`,
  ];
  return parts.join('\n');
}

export const swarmOrchestratorModule: BenchmarkModule = {
  id: 'swarmOrchestrator',
  name: 'Swarm Orchestrator',
  description:
    'Validates that SwarmOrchestrator manager-worker-critic-fission-fusion recursion ' +
    'outperforms a single-agent single-turn baseline on complex decomposable goals.',
  path: 'swarm/swarmOrchestrator.ts',
  baselineFactory: () => ({
    execute: (goal: string) =>
      Promise.resolve({
        summary: goal,
        totalTokensUsed: 0,
      }),
  }),
  treatmentFactory: () => {
    const provider = createDeterministicSwarmProvider();
    const config: Partial<SwarmConfig> = {
      ...DEFAULT_SWARM_CONFIG,
      model: 'scripted-swarm',
      fissionThreshold: 5,
      maxDepth: 2,
      goalConfig: {
        mode: 'quick',
        maxRounds: 5,
        budgetTokens: TASK_BUDGET_TOKENS,
      },
    };
    return {
      orchestrator: new SwarmOrchestrator(provider, config, 0),
    };
  },
  runTrial: async ({ implementation, task }) => {
    const impl = implementation as {
      execute?: (goal: string) => Promise<{ summary: string; totalTokensUsed: number }>;
      orchestrator?: SwarmOrchestrator;
    };

    if (impl.orchestrator) {
      const result = await impl.orchestrator.execute(task.prompt);
      const output = buildTreatmentOutput(result);
      return {
        output,
        tokenUsage: {
          input: result.totalTokensUsed,
          output: 0,
          total: result.totalTokensUsed,
          cached: 0,
          reasoning: 0,
        },
        // Hold latency constant so the comparison is driven by success rate
        // and cost, not by execution timing variance.
        latencyMs: 1,
      };
    }

    const baseline = await impl.execute!(task.prompt);
    return {
      output: baseline.summary,
      tokenUsage: {
        // Simulate an expensive single-turn monolithic prompt so the baseline
        // is worse on cost than the decomposed treatment.
        input: 4000,
        output: 1000,
        total: 5000,
        cached: 0,
        reasoning: 0,
      },
      latencyMs: 1,
    };
  },
  taskSuite,
  metrics: ['successRate'],
};
